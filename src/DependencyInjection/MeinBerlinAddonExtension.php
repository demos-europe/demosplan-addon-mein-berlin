namespace DemosEurope\DemosplanAddon\DemosMeinBerlin\DependencyInjection;

use Symfony\Component\Config\FileLocator;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Loader\YamlFileLoader;
use DemosEurope\DemosplanAddon\DependencyInjection\AbstractAddonExtension;

class MeinBerlinAddonExtension extends AbstractAddonExtension
{
    /**
     * Loads a specific configuration.
     *
     * @param array            $configs   An array of configuration values
     * @param ContainerBuilder $container A ContainerBuilder instance
     *
     * @throws \InvalidArgumentException When provided tag is not defined in this extension
     * @throws \Exception
     */
    protected function loadFullAddon(array $configs, ContainerBuilder $container): void
    {
        $loader = new YamlFileLoader(
            $container,
            new FileLocator(__DIR__.'/../../config')
        );
        $loader->load('services.yml');
        $loader->load('parameters_default.yml');
        $loader->load('parameters_test.yml');
    }
}
